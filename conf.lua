love.conf = function(t)
    t.console = true        -- Enable the debug console for Windows.
    t.window.width = 1600    -- Set the width of the window
    t.window.height = 900  -- Set the height of the window
    t.window.title = "awesome chess"
    t.window.vsync = 1 -- Enable vertical sync
    t.window.fullscreen = false
    t.window.resizable = false
  end